# Time converter

# Ваша задача - переконвертировать время из 24-часового формата в 12-часовой, используя следующие
# правила: выходной формат должен быть 'чч:мм a.m.' (для часов до полудня) или 'чч:мм p.m.'
# (для часов после полудня), если часы меньше 10 - не пишите '0' перед ними. Например: '9:05 a.m.'

time = input("What's the time?")
analysis = time.split(':')

if int(analysis[0]) > 23 or\
   int(analysis[0]) < 0 or\
   int(analysis[1]) > 59 or\
   int(analysis[1]) < 0:
    print("It's time to fix your watch!")
else:
    if int(analysis[0]) == 0:
        print(f'12:{analysis[1]} a.m.')
    elif int(analysis[0]) > 0 and int(analysis[0]) < 10:
        print(f'{int(analysis[0])}:{analysis[1]} a.m')
    elif int(analysis[0]) >= 10 and int(analysis[0]) < 12:
        print(f'{time} a.m.')
    elif int(analysis[0]) > 12:
        print(f'{int(analysis[0]) - 12}:{analysis[1]} p.m.')
    else:
        print(f'{time} p.m.')
