# Time

# Однажды ночью вы отправляетесь кататься на мотоцикле. В 00:00 вы запускаете
# двигатель, и встроенный таймер автоматически начинает отсчитывать продолжительность
# вашей поездки в минутах. Вы отправляетесь исследовать окрестности. Когда вы, наконец,
# решаете вернуться назад, вы понимаете, что есть вероятность, что мосты на вашем пути домой
# разведены, и вы останетесь в затруднительном положении! К сожалению, у вас нет с собой часов
# и вы не знаете, который час. Благодаря таймеру мотоцикла вы знаете только то, что с 00:00
# прошло n минут.
# Используя таймер мотоцикла, рассчитайте текущее время. Возвращает ответ в виде суммы цифр,
# которую покажет цифровой таймер в формате чч:мм.

timer = int(input('Timer shows: '))

hours = timer // 60
minutes = timer % 60
result = hours // 10 + hours % 10 + minutes // 10 + minutes % 10
print(result)


